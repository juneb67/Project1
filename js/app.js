$( "#pic1" ).hover(function() {
  $( "#pic1" ).fadeTo( "fast", 0.4 )
});
//When the mouse leaves, remove fade
$( "#pic1" ).mouseleave(function() {
  $( "#pic1" ).fadeTo( "fast", 1.0 )
});

